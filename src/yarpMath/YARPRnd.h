/// Return a random number between 0 and 1;

#ifndef __YARPRND__
#define __YARPRND__

// RAN1 from NR
#define IA 16807
#define IM 2147483647
#define AM (1.0/IM)
#define IQ 127773
#define IR 2836
#define NTAB 32
#define NDIV (1+(IM-1)/NTAB)
#define EPS 1.2e-7
#define RNMX (1.0-EPS)

class YARPRnd
{
 public:
	YARPRnd(int seed);
	YARPRnd();
	
	void init();
	void init (int seed);
	long getSeed ()
	{ return idum; }

	double getNumber();
	
private:
	int idum;
	int iy;
	int iv[NTAB];
};

#endif

